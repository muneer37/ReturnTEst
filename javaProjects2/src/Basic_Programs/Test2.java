package Basic_Programs;
class  Test1
{
	public static void main(String[] args) 
	{
		System.out.println("\n");
		for(int i=5;i>=0;i--)
		{
		System.out.println("Hello World!");
		}
		System.out.println("\n--------------------------------------------\n ");

		for(int i=10;i<6;i--)
		{
		System.out.println("Hello World!");
		}
		
		System.out.println("\n-------------------Nested For-------------------------\n ");
			
		for(int i=5;i>=4;i--)
		{
		for(int j=2;j<4;j++)
		{
		System.out.println("Hello World!");
		}
		}

	}
}
