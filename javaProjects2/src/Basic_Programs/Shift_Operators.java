package Basic_Programs;
class Shift_Operators
{
	public static void main(String[] args) 
	{
		System.out.println("main method started");
		
		System.out.println("value is : "+(20<<2));//20/2^2=20*4=80
		System.out.println(" value is : "+(20<<3));
		System.out.println(" value is : "+(20<<4));
		System.out.println(" value is : "+(540<<6)); //540/2^6=540*64=34,560
	
        System.out.println("main method ended");
	}
}